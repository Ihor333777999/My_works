package String;

public class Compare {
    public static void main(String[] args) {
        String word = "java";
        String word2 = "james";
        String world3 = "5java";
        System.out.println(word.compareTo(word2));
        System.out.println(word2.compareTo(word));
        System.out.println(word.compareTo(world3));
    }
}
